        private static string NormalizeForMatch(string? s)
        {
            if (string.IsNullOrWhiteSpace(s)) return string.Empty;
            var lowered = s.Trim().ToLowerInvariant();
            // remove common punctuation and whitespace for looser comparison
            var cleaned = new string(lowered.Where(c => char.IsLetterOrDigit(c)).ToArray());
            return cleaned;
        }

        private static bool IsCategoryMatch(string examCategory, string questionCategory)
        {
            if (string.IsNullOrWhiteSpace(examCategory) || string.IsNullOrWhiteSpace(questionCategory)) return false;
            var a = NormalizeForMatch(examCategory);
            var b = NormalizeForMatch(questionCategory);
            if (a == b) return true;
            if (a.Contains(b) || b.Contains(a)) return true;
            return false;
        }

