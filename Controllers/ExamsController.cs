        [HttpGet("{id}/full")]
        [AllowAnonymous]
        public async Task<IActionResult> GetFullExam(int id)
        {
            try
            {
                var exam = await _context.Exams.AsNoTracking().FirstOrDefaultAsync(e => e.ExamId == id);
                if (exam == null)
                {
                    return NotFound(new { message = "Không tìm thấy đề thi." });
                }

                // Nếu đề thi chưa công bố và người dùng không phải Admin thì chặn
                bool isAdmin = User.Identity?.IsAuthenticated == true && User.IsInRole("Admin");
                if (exam.Status != "Published" && !isAdmin)
                {
                    return BadRequest(new { message = "Đề thi này chưa được công bố." });
                }

                var questions = await (from eq in _context.ExamQuestions
                                       join q in _context.QuestionBank on eq.QuestionId equals q.QuestionId
                                       where eq.ExamId == id
                                       orderby eq.OrderIndex
                                       select new
                                       {
                                           questionId = q.QuestionId,
                                           content = q.Content,
                                           optionA = q.OptionA,
                                           optionB = q.OptionB,
                                           optionC = q.OptionC,
                                           optionD = q.OptionD,
                                           correctOption = q.CorrectOption,
                                           scorePerQuestion = q.ScorePerQuestion,
                                           orderIndex = eq.OrderIndex,
                                           imageUrl = q.ImageUrl
                                       }).ToListAsync();

                return Ok(new
                {
                    ExamInfo = exam,
                    TotalQuestions = questions.Count,
                    Questions = questions
                });
            }
            catch (Exception ex)
            {
                return StatusCode(500, new { message = "Lỗi hệ thống khi lấy đề thi đầy đủ câu hỏi.", error = ex.Message });
            }
        }



        [HttpPost]
        [Authorize(Roles = "Admin")]
        public async Task<IActionResult> CreateExam([FromBody] CreateExamRequest request)
        {
            try
            {
                if (request == null)
                    return BadRequest(new { message = "Dữ liệu đề thi không hợp lệ." });
                
                var newExam = new Exam
                {
                    Title = request.Title,
                    Description = request.Description,
                    Category = request.Category,
                    Level = request.Level,
                    TimeLimit = request.TimeLimit,
                    TotalScore = request.TotalScore,
                    Status = request.Status ?? "Draft",
                    CreatedBy = User.Identity?.Name ?? User.Claims.FirstOrDefault(c => c.Type == "sub" || c.Type.Contains("name"))?.Value ?? "admin",
                    CreatedAt = DateTime.Now
                };

                await _context.Exams.AddAsync(newExam);
                await _context.SaveChangesAsync();

                // Tự động gán câu hỏi nếu có QuestionCount
                if (request.QuestionCount.HasValue && request.QuestionCount.Value > 0)
                {
                    // Load toàn bộ câu active, filter in-memory để dùng case-insensitive
                    var allActive = await _context.QuestionBank
                        .Where(q => q.IsActive == true)
                        .ToListAsync();

                    var catNorm = (newExam.Category ?? "").Trim().ToLowerInvariant();
                    var lvlNorm = (newExam.Level ?? "").Trim().ToLowerInvariant();

                    // 1. Ưu tiên đúng cả category + level
                    var matched = allActive
                        .Where(q =>
                            (q.Category ?? "").Trim().ToLowerInvariant() == catNorm &&
                            (q.Level ?? "").Trim().ToLowerInvariant() == lvlNorm)
                        .ToList();

                    // 2. Fallback: đúng category, bất kỳ level
                    if (matched.Count < request.QuestionCount.Value)
                    {
                        var anyLevel = allActive
                            .Where(q => (q.Category ?? "").Trim().ToLowerInvariant() == catNorm &&
                                        !matched.Any(m => m.QuestionId == q.QuestionId))
                            .ToList();
                        matched.AddRange(anyLevel);
                    }

                    var randomQuestions = matched
                        .OrderBy(_ => Guid.NewGuid())
                        .Take(request.QuestionCount.Value)
                        .ToList();

                    for (int i = 0; i < randomQuestions.Count; i++)
                    {
                        await _context.ExamQuestions.AddAsync(new ExamQuestion
                        {
                            ExamId = newExam.ExamId,
                            QuestionId = randomQuestions[i].QuestionId,
                            OrderIndex = i + 1
                        });
                    }
                    await _context.SaveChangesAsync();
                }

                if (newExam.Status == "Published")
                {
                    var notification = new Notification
                    {
                        Title = "Đề thi mới xuất bản",
                        Message = $"Đề thi \"{newExam.Title}\" ({newExam.Category} - {newExam.Level}) đã được đăng tải. Thử sức ngay!",
                        Type = "Exam",
                        TargetId = newExam.ExamId,
                        IsRead = false,
                        CreatedAt = DateTime.Now
                    };
                    await _context.Notifications.AddAsync(notification);
                    await _context.SaveChangesAsync();
                }

                return CreatedAtAction(nameof(GetExamById), new { id = newExam.ExamId }, newExam);
            }
            catch (Exception ex)
            {
                var innerMsg = ex.InnerException != null ? ex.InnerException.Message : "";
                return StatusCode(500, new { message = "Lỗi hệ thống khi tạo đề thi.", error = ex.Message, innerError = innerMsg });
            }
        }

        [HttpPost("{id}/questions/{questionId}")]
        [Authorize(Roles = "Admin")]
        public async Task<IActionResult> AddQuestion(int id, int questionId, [FromQuery] int? index)
        {
            try
            {
                var exists = await _context.ExamQuestions.AnyAsync(eq => eq.ExamId == id && eq.QuestionId == questionId);
                if (exists) return BadRequest(new { message = "Câu hỏi đã có trong đề thi." });

                // Determine insertion index. If not provided, append to end.
                var maxOrder = await _context.ExamQuestions
                    .Where(eq => eq.ExamId == id)
                    .MaxAsync(eq => (int?)eq.OrderIndex) ?? 0;

                int insertAt = index.HasValue ? Math.Max(1, Math.Min(index.Value, maxOrder + 1)) : (maxOrder + 1);

                // Shift existing items at or after insertAt
                var toShift = await _context.ExamQuestions.Where(eq => eq.ExamId == id && eq.OrderIndex >= insertAt).ToListAsync();
                foreach (var s in toShift)
                {
                    s.OrderIndex = s.OrderIndex + 1;
                    _context.ExamQuestions.Update(s);
                }

                var link = new ExamQuestion
                {
                    ExamId = id,
                    QuestionId = questionId,
                    OrderIndex = insertAt
                };

                await _context.ExamQuestions.AddAsync(link);
                await _context.SaveChangesAsync();
                return Ok(new { message = "Đã thêm câu hỏi vào đề." });
            }
            catch (Exception ex)
            {
                return StatusCode(500, new { message = "Lỗi khi thêm câu hỏi.", error = ex.Message });
            }
        }

        [HttpPut("{id}/questions/reorder")]
        [Authorize(Roles = "Admin")]
        public async Task<IActionResult> ReorderQuestions(int id, [FromBody] List<int> orderedQuestionIds)
        {
            try
            {
                if (orderedQuestionIds == null) return BadRequest(new { message = "Dữ liệu không hợp lệ." });

                var current = await _context.ExamQuestions.Where(eq => eq.ExamId == id).ToListAsync();
                var currentIds = current.Select(c => c.QuestionId).ToHashSet();
                // Validate the provided list contains exactly the same IDs
                if (!orderedQuestionIds.All(i => currentIds.Contains(i)) || orderedQuestionIds.Count != current.Count)
                {
                    return BadRequest(new { message = "Danh sách câu hỏi không khớp với đề thi hiện tại." });
                }

                for (int i = 0; i < orderedQuestionIds.Count; i++)
                {
                    var qId = orderedQuestionIds[i];
                    var link = current.First(eq => eq.QuestionId == qId);
                    link.OrderIndex = i + 1;
                    _context.ExamQuestions.Update(link);
                }

                await _context.SaveChangesAsync();
                return Ok(new { message = "Sắp xếp câu hỏi thành công." });
            }
            catch (Exception ex)
            {
                return StatusCode(500, new { message = "Lỗi khi sắp xếp câu hỏi.", error = ex.Message });
            }
        }

