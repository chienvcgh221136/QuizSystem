        [HttpDelete("{id}")]
        [Authorize(Roles = "Admin")]
        public async Task<IActionResult> DeleteExam(int id)
        {
            try
            {
                var exam = await _context.Exams.FindAsync(id);
                if (exam == null)
                {
                    return NotFound(new { message = "Không tìm thấy đề thi để xóa." });
                }

                // 1. Xóa UserAnswers liên quan đến các ExamResult của đề này
                var resultIds = await _context.ExamResults
                    .Where(r => r.ExamId == id)
                    .Select(r => r.ResultId)
                    .ToListAsync();

                if (resultIds.Any())
                {
                    var userAnswers = await _context.UserAnswers
                        .Where(a => resultIds.Contains(a.ResultId))
                        .ToListAsync();
                    if (userAnswers.Any())
                        _context.UserAnswers.RemoveRange(userAnswers);

                    // 2. Xóa ExamResults
                    var examResults = await _context.ExamResults
                        .Where(r => resultIds.Contains(r.ResultId))
                        .ToListAsync();
                    _context.ExamResults.RemoveRange(examResults);
                }

                // 3. Xóa ExamQuestions
                var examQuestions = await _context.ExamQuestions
                    .Where(eq => eq.ExamId == id)
                    .ToListAsync();
                if (examQuestions.Any())
                    _context.ExamQuestions.RemoveRange(examQuestions);

                // 4. Xóa Exam
                _context.Exams.Remove(exam);

                await _context.SaveChangesAsync();

                return Ok(new { message = "Xóa đề thi và các liên kết thành công." });
            }
            catch (Exception ex)
            {
                var innerMsg = ex.InnerException != null ? ex.InnerException.Message : "";
                return StatusCode(500, new { message = "Lỗi hệ thống khi xóa đề thi.", error = ex.Message, innerError = innerMsg });
            }
        }

        [HttpPost("full")]
        [Authorize(Roles = "Admin")]
        public async Task<IActionResult> CreateFullExam([FromBody] CreateFullExamRequest request)
        {
            using var transaction = await _context.Database.BeginTransactionAsync();
            try
            {
                var newExam = new Exam
                {
                    Title = request.Title,
                    Category = request.Category,
                    Level = request.Level,
                    TimeLimit = request.TimeLimit,
                    TotalScore = request.TotalScore,
                    Status = request.Status,
                    CreatedBy = request.CreatedByAI ? "AI" : (User.Identity?.Name ?? "admin"),
                    CreatedAt = DateTime.Now
                };

                await _context.Exams.AddAsync(newExam);
                await _context.SaveChangesAsync();

                int order = 1;
                foreach (var qReq in request.Questions)
                {
                    // Sanitize Answer: Ensure it is "A", "B", "C", or "D"
                    string NormalizeAnswer(string? answer, List<string> options)
                    {
                        if (string.IsNullOrWhiteSpace(answer)) return "A";
                        var a = answer.Trim();
                        // If starts with letter A-D (with optional punctuation), use it
                        if (a.Length > 0)
                        {
                            var first = char.ToUpperInvariant(a[0]);
                            if (first >= 'A' && first <= 'D') return first.ToString();
                        }

                        // Try to match exact option text (case-insensitive, trimmed)
                        for (int i = 0; i < options.Count; i++)
                        {
                            var opt = (options[i] ?? string.Empty).Trim();
                            if (string.Equals(opt, a, StringComparison.OrdinalIgnoreCase)) return ((char)('A' + i)).ToString();
                        }

                        // Try contains (option text within answer)
                        for (int i = 0; i < options.Count; i++)
                        {
                            var opt = (options[i] ?? string.Empty).Trim();
                            if (!string.IsNullOrEmpty(opt) && a.IndexOf(opt, StringComparison.OrdinalIgnoreCase) >= 0) return ((char)('A' + i)).ToString();
                        }

                        // Fallback to first option
                        return "A";
                    }

                    string sanitizedAnswer = NormalizeAnswer(qReq.Answer, qReq.Options);

                    var newQuestion = new QuestionBank
                    {
                        Content = qReq.Text,
                        Category = request.Category,
                        Level = request.Level,
                        OptionA = qReq.Options.Count > 0 ? qReq.Options[0] : "",
                        OptionB = qReq.Options.Count > 1 ? qReq.Options[1] : "",
                        OptionC = qReq.Options.Count > 2 ? qReq.Options[2] : "",
                        OptionD = qReq.Options.Count > 3 ? qReq.Options[3] : "",
                        CorrectOption = sanitizedAnswer,
                        ScorePerQuestion = (double)request.TotalScore / request.Questions.Count,
                        ImageUrl = qReq.ImageUrl,  // Lưu URL ảnh Cloudinary
                        CreatedAt = DateTime.Now
                    };

                    await _context.QuestionBank.AddAsync(newQuestion);
                    await _context.SaveChangesAsync();

                    await _context.ExamQuestions.AddAsync(new ExamQuestion
                    {
                        ExamId = newExam.ExamId,
                        QuestionId = newQuestion.QuestionId,
                        OrderIndex = order++
                    });
                }

                await _context.SaveChangesAsync();

                if (newExam.Status == "Published")
                {
                    var notification = new Notification
                    {
                        Title = "Đề thi mới xuất bản",
                        Message = $"Đề thi \"{newExam.Title}\" ({newExam.Category} - {newExam.Level}) đã được đăng tải. Thử sức ngay!",
                        Type = "Exam",
                        TargetId = newExam.ExamId,
                        IsRead = false,
                        CreatedAt = DateTime.Now
                    };
                    await _context.Notifications.AddAsync(notification);
                    await _context.SaveChangesAsync();
                }

                await transaction.CommitAsync();

                return Ok(new { message = "Đã lưu bộ đề thi thành công!", examId = newExam.ExamId });
            }
            catch (Exception ex)
            {
                await transaction.RollbackAsync();
                var innerMsg = ex.InnerException != null ? ex.InnerException.Message : "";
                Console.WriteLine($"[CreateFullExam Error] {ex.Message}. Inner: {innerMsg}");
                return StatusCode(500, new { message = "Lỗi khi lưu bộ đề đầy đủ.", error = ex.Message, innerError = innerMsg });
            }
        }

