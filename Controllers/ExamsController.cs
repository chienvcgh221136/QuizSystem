        [HttpGet("{id}/available-questions")]
        [Authorize(Roles = "Admin")]
        public async Task<IActionResult> GetAvailableQuestions(int id)
        {
            try
            {
                var exam = await _context.Exams.FindAsync(id);
                if (exam == null) return NotFound();

                var currentQuestionIds = await _context.ExamQuestions
                    .Where(eq => eq.ExamId == id)
                    .Select(eq => eq.QuestionId)
                    .ToListAsync();

                // Load candidate questions (exclude ones already in the exam)
                var candidates = await _context.QuestionBank
                    .Where(q => !currentQuestionIds.Contains(q.QuestionId) && q.IsActive)
                    .ToListAsync();

                // Filter by fuzzy category match and exact (case-insensitive) level match
                var matched = candidates.Where(q => IsCategoryMatch(exam.Category ?? string.Empty, q.Category ?? string.Empty)
                                                    && string.Equals((q.Level ?? string.Empty).Trim(), (exam.Level ?? string.Empty).Trim(), StringComparison.OrdinalIgnoreCase))
                                       .ToList();

                return Ok(matched);
            }
            catch (Exception ex)
            {
                return StatusCode(500, new { message = "Lỗi khi lấy danh sách câu hỏi.", error = ex.Message });
            }
        }

